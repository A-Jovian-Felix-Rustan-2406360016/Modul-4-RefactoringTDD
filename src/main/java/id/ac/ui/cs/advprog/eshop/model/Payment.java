package id.ac.ui.cs.advprog.eshop.model;

import id.ac.ui.cs.advprog.eshop.enums.PaymentStatus;
import lombok.Getter;
import java.util.Map;

@Getter
public class Payment {
    private String id;
    private Order order;
    private String method;
    private String status;
    private Map<String, String> paymentData;

    public Payment(String id, Order order, String method, Map<String, String> paymentData) {
        this.id = id;
        this.order = order;
        this.method = method;
        this.paymentData = paymentData;

        if (order == null || method == null || method.isEmpty() || paymentData == null || paymentData.isEmpty()) {
            throw new IllegalArgumentException();
        }
        this.status = PaymentStatus.WAITING_PAYMENT.getValue();
    }

    public Payment(String id, Order order, String method, String status, Map<String, String> paymentData) {
        this(id, order, method, paymentData);
        this.setStatus(status);
    }

    public void setStatus(String status) {
        if (PaymentStatus.contains(status)) {
            this.status = status;
        } else {
            throw new IllegalArgumentException();
        }
    }
}